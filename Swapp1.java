 class Swapp1
 {
   public static void main(String[]args)
    {
     int a=14;
     int b=47;
     int c=0;
     
     c=a;
     a=b;
     b=c;
     
     System.out.println("before swapping");
     System.out.println("a=14 b=47");
     
     System.out.println("after swap");
     System.out.println("a=" +a);
     System.out.println("b=" +b);
    }
 }
   